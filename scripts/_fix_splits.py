"""根据人工审查结果批量修复拆分误配。

规则: 色名的两个字分属前后两个不同的词，
例如"扫石绿花斑"中"石"属"扫石"、"绿"属"绿花斑"。
"""

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
POETRY = ROOT / "data" / "poetry"

def load(pinyin):
    return json.loads((POETRY / f"{pinyin}.json").read_text(encoding="utf-8"))

def save(pinyin, entries):
    data = load(pinyin)
    data["entries"] = entries
    data["coverage"] = f"{len(entries)}/5"
    (POETRY / f"{pinyin}.json").write_text(
        json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8"
    )

def remove_entry(pinyin, index):
    """按索引删除单条 entry（0-based）"""
    d = load(pinyin)
    entries = d["entries"]
    if index < len(entries):
        removed = entries.pop(index)
        d["entries"] = entries
        d["coverage"] = f"{len(entries)}/5"
        (POETRY / f"{pinyin}.json").write_text(
            json.dumps(d, ensure_ascii=False, indent=2) + "\n", encoding="utf-8"
        )
        print(f"  - {pinyin}: [{removed['line'][:30]}...] ({removed['author']})")
    return len(entries)

