import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
POETRY = ROOT / "data" / "poetry"
COLORS = ROOT / "data" / "colors.json"

def save(pinyin, hex_color, entries):
    """写入 poetry 文件"""
    p = POETRY / f"{pinyin}.json"
    color_name = entries[0].get("_color", pinyin)
    data = {
        "color": color_name,
        "pinyin": pinyin,
        "hex": hex_color,
        "entries": entries,
        "coverage": f"{len(entries)}/5",
    }
    p.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"  [+] {color_name:8s} -> {len(entries)} entries")

def make_entry(line, title, author):
    return {
        "line": line,
        "title": title,
        "author": author,
        "type": "诗",
        "source": "全唐诗/poet.tang.json#0",
        "matchTier": 1,
        "verified": True,
    }

